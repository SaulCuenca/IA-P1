# Añade a la siguiente lista los colores 'magenta' y 'turquesa' utilizando insert().
# Tendrás que posicionar 'magenta' en la posición siguiente a la de 'lila' y 'turquesa' en la penúltima posición.
# Deberás hacer esto utilizando posiciones de lista negativa

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']#Creamos una lista con colores mencionados


colores.insert(-4,'magenta') #Agregamos el color magenta en la posicion siguiente de lilacon numeros negativos
colores.insert(-1,'turquesa') #Agregamos el color turquesa en la penultima posicion con numeros negativos

print(colores) #Mostramos en pantalla nuestra lista para ver los cambios 
