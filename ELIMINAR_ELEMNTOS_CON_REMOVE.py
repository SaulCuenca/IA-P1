# Elimina de la siguiente lista los elementos 'amarillo' y 'rojo'.
# Solo puedes eliminarlos utilizando el método remove().

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marron', 'lila', 'negro', 'rosa', 'blanco', 'naranja']#Creamos lista con los colores mencionados


colores.remove('amarillo') #Eliminamos el amarillo con remove
colores.remove("rojo") #Eliminamos el rojo con remove

print(colores) #Mostramos en pantalla la lista para ver que colores nos quedaron
